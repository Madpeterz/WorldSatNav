local function point_on_segment(x, y, x1, y1, x2, y2)
  local dx = x2 - x1
  local dy = y2 - y1
  local len_sq = dx * dx + dy * dy
  if len_sq == 0 then
    return math.abs(x - x1) <= 1e-9 and math.abs(y - y1) <= 1e-9
  end

  local cross = (x - x1) * dy - (y - y1) * dx
  if math.abs(cross) > 1e-9 then
    return false
  end

  local dot = (x - x1) * dx + (y - y1) * dy
  if dot < 0 then
    return false
  end

  return dot <= len_sq
end

local function point_in_polygon_single_ray(x, y, poly, y_epsilon)
    local inside = false
    local yq = y + y_epsilon
    local n = #poly
    local j = n

    for i = 1, n do
        local xi, yi = poly[i][1], poly[i][2]
        local xj, yj = poly[j][1], poly[j][2]
        if ((yi > yq) ~= (yj > yq)) and
           (x < (xj - xi) * (yq - yi) / (yj - yi) + xi) then
            inside = not inside
        end
        j = i
    end

    return inside
end

local function point_in_polygon(x, y, poly)
    -- Multi-ray vote reduces instability for points near vertices.
    local n = #poly
    if n < 3 then
        return false
    end

    local j = n
    for i = 1, n do
        local xi, yi = poly[i][1], poly[i][2]
        local xj, yj = poly[j][1], poly[j][2]
        if point_on_segment(x, y, xi, yi, xj, yj) then
            return true
        end
        j = i
    end

    local hits = 0
    local rays = {0.0, 1e-7, -1e-7}
    for _, eps in ipairs(rays) do
        if point_in_polygon_single_ray(x, y, poly, eps) then
            hits = hits + 1
        end
    end
    return hits >= 2
end

local function point_in_shape(x, y, polygons)
    -- Even-odd fill rule across subpaths (handles holes reliably).
    local inside = false
    for _, polygon in ipairs(polygons) do
        if point_in_polygon(x, y, polygon) then
            inside = not inside
        end
    end
    return inside
end

local function is_better_shape(shape, best_shape)
    if best_shape == nil then
        return true
    end
    if shape.priority ~= best_shape.priority then
        return shape.priority < best_shape.priority
    end
    if shape.draw_order ~= best_shape.draw_order then
        return shape.draw_order > best_shape.draw_order
    end
    if shape.area ~= best_shape.area then
        return shape.area < best_shape.area
    end
    if shape.bbox_area ~= best_shape.bbox_area then
        return shape.bbox_area < best_shape.bbox_area
    end
    if shape.vertex_count ~= best_shape.vertex_count then
        return shape.vertex_count < best_shape.vertex_count
    end
    return shape.name < best_shape.name
end

local function getShapeAt(x, y)
    local best_shape = nil

    for _, shape in ipairs(shapesData) do
        local bb = shape.bbox
        if x >= bb[1] and x <= bb[3] and y >= bb[2] and y <= bb[4] then
            if point_in_shape(x, y, shape.polygons) then
                if is_better_shape(shape, best_shape) then
                    best_shape = shape
                end
            end
        end
    end

    if best_shape ~= nil then
        return best_shape.name
    end

    return "?"
end


local function convertSextantToMapCoordinates(sextant)
	-- sextant coordinate structure with longitude, latitude, deg_long, min_long, sec_long, deg_lat, min_lat, sec_lat
	if not sextant then
		helpers.DevLog("Cannot convert sextant to map coordinates, sextant is nil")
		return nil, nil
	end
  local renderSettings = { zeroPointX=2515,zeroPointY=989,XCordScale=116.35,YCordScale=115.21}

	local long = sextant.longitude
	local lat = sextant.latitude

	local longValue = 0
	local latValue = 0
	if long == nil or lat == nil then
		helpers.DevLog("Invalid sextant data, missing longitude or latitude direction")
		return nil, nil
	end

	local degLong = sextant.deg_long
	local minLong = sextant.min_long
	local secLong = sextant.sec_long
	local degLat = sextant.deg_lat
	local minLat = sextant.min_lat
	local secLat = sextant.sec_lat
	if degLong == nil or minLong == nil or secLong == nil or degLat == nil or minLat == nil or secLat == nil then
		helpers.DevLog("Invalid sextant data, cannot convert to map coordinates")
		return nil, nil
	end
	longValue = degLong + (minLong / 60) + (secLong / 3600)
	latValue = degLat + (minLat / 60) + (secLat / 3600)

	if sextant.longitude == "W" then
		longValue = -longValue
	end
	if sextant.latitude == "N" then
		latValue = -latValue
	end

	local x = renderSettings.zeroPointX + (longValue * renderSettings.XCordScale)
	local y = renderSettings.zeroPointY + (latValue * renderSettings.YCordScale)
	return x, y
end

function regionmap.GetRegionForSextant(sextant)
  if sextant == nil then return "?", "?" end
  local x, y = convertSextantToMapCoordinates(sextant)
  if x == nil or y == nil then return "?", "?" end
  local regionnameRaw = getShapeAt(x, y)
  local regionCode, regionName = string.match(regionnameRaw, "^([^/]+)/(.+)$")
  if regionCode ~= nil and regionName ~= nil then
    return regionCode, regionName
  end
  return "?", regionnameRaw
end

